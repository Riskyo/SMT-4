// Operator Pembanding
let x = 8;
let y = 7;

// Lebih Besar
console.log(x > y);

// Lebih Kecil
console.log(x < y);

// Sama dengan
console.log(x == y);

// Lebih besar sama dengan
console.log(x >= y);

// Lebih kecil sama dengan
console.log(x <= y);

